public class MyDivision
{
    /**
     * Esta clase nos permite dividir.
     * @param n (numerador)
     * @param d (denominador)
     * @return mediante un int devolvemos el resultado.
     */
    public static int division(int n, int d)
    {
        int r;
        r = n / d;
        return r;
    }
}
